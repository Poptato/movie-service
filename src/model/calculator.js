class Calculator {
    static add(x, y) {
        return x + y;
    }

    static sub(x, y) {
        return x - y;
    }

    static mul(x, y) {
        return x * y;
    }

    static div(x, y) {
        return x / y;
    }
}

module.exports = Calculator;
